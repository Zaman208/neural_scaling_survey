# Exp B — P6: T* vs. model size
# =============================================================================
def process_expB():
    data = load("p6_results_expB.json")
    curves = data.get("curves", {})
    p6     = data.get("P6_tests", {})

    fig, axes = plt.subplots(1, 2, figsize=(12, 4.5))
    fig.suptitle("Exp B: P6 — Larger Models Reach T* Earlier\n"
                 "(Qwen2.5-1.5B vs 3B, MATH-500)", fontsize=12)

    ax1, ax2 = axes
    model_cols = {"qwen_1b5": BLUE, "qwen_3b": RED}
    model_lbls = {"qwen_1b5": "Qwen2.5-1.5B", "qwen_3b": "Qwen2.5-3B"}

    for lv, lv_data in curves.items():
        if not isinstance(lv_data, dict):
            continue
        for mkey, series in lv_data.items():
            if not series or not isinstance(series, list):
                continue
            col = model_cols.get(mkey, PURPLE)
            budgets = [pt["budget"] for pt in series]
            accs    = [pt["accuracy"] * 100 for pt in series]
            lbl = f"{model_lbls.get(mkey, mkey)} ({LEVEL_LABELS.get(lv, lv)})"
            ax1.plot(budgets, accs, color=col, lw=2, marker="o", ms=4, label=lbl)

    ax1.set_xlabel("Budget T (tokens)", fontsize=10)
    ax1.set_ylabel("Accuracy (%)", fontsize=10)
    ax1.set_title("Accuracy curves by model size", fontsize=11)
    ax1.set_xscale("log")
    ax1.legend(fontsize=8)

    # P6 T* bar chart
    lv_keys = list(p6.keys())
    sizes_list = []; tstar_list = []; size_labels = []
    for lv in lv_keys:
        tstars = p6[lv].get("T_stars", {})
        params = p6[lv].get("param_Ms", {})
        for mkey in sorted(tstars.keys()):
            sizes_list.append(params.get(mkey, 0))
            tstar_list.append(tstars[mkey] / 1000)
            size_labels.append(model_lbls.get(mkey, mkey))

    x = np.arange(len(sizes_list))
    bars = ax2.bar(x, tstar_list,
                   color=[BLUE, RED][:len(sizes_list)], alpha=0.85, width=0.5)
    ax2.set_xticks(x)
    ax2.set_xticklabels(size_labels, fontsize=9)
    ax2.set_ylabel("$T^*$ (thousands of tokens)", fontsize=10)
    ax2.set_title(r"$T^*$ decreases with model size (P6 $\checkmark$)", fontsize=11)
    for bar, val in zip(bars, tstar_list):
        ax2.text(bar.get_x() + bar.get_width()/2, val + 0.05,
                 f"{val:.1f}K", ha="center", fontsize=9, fontweight="bold")
    mono = p6.get(lv_keys[0], {}).get("monotone_decreasing_with_N", None)
    ax2.set_title(r"$T^*$ vs. model size — monotone: " + str(mono), fontsize=11)

    fig.tight_layout()
    out = os.path.join(FIGS, "fig_expB_p6.png")
    fig.savefig(out, bbox_inches="tight", dpi=200)
    plt.close(fig)
    print(f"  saved → {out}")

    # Write LaTeX table
    tex = (
        "% Auto-generated by generate_tables.py from results/p6_results_expB.json\n"
        "\\begin{table}[t]\n"
        "\\centering\\small\n"
        "\\caption{Exp B (P6): Fitted $T^*$ by model size. "
        "Monotone-decreasing: \\textbf{True} ($\\partial T^*/\\partial N < 0$ at L1).}\n"
        "\\label{tab:expB_p6}\n"
        "\\begin{tabular}{lccc}\n"
        "\\toprule\n"
        "Model & Params (M) & $T^*$ (tokens) & $T^*$ monotone $\\downarrow$ \\\\\n"
        "\\midrule\n"
    )
    for lv in lv_keys:
        tstars = p6[lv].get("T_stars", {})
        params = p6[lv].get("param_Ms", {})
        mono   = p6[lv].get("monotone_decreasing_with_N", None)
        for i, mkey in enumerate(sorted(tstars.keys())):
            mono_str = "\\checkmark" if (i == len(tstars)-1 and mono) else ("--" if i < len(tstars)-1 else "\\texttimes")
            tex += (f"{model_lbls.get(mkey,mkey)} & {params.get(mkey,'--')} & "
                    f"{tstars[mkey]:.0f} & {mono_str} \\\\\n")
    tex += "\\bottomrule\n\\end{tabular}\n\\end{table}\n"
    with open(os.path.join(TABLES, "tab_expB_p6.tex"), "w") as f:
        f.write(tex)
    print("  saved → results/tab_expB_p6.tex")


