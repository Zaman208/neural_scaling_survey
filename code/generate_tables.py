# Exp D — Precision sweep (P3 test)
# =============================================================================
def process_expD():
    data  = load("precision_results_expD.json")
    p3    = data.get("P3_tests", {})

    fig, axes = plt.subplots(1, 2, figsize=(12, 4.5))
    fig.suptitle("Exp D: T* by Precision and Difficulty (P3 test)\n"
                 "(Phi-3.5-mini, MATH-500)", fontsize=12)

    prec_order = ["fp16", "bf16", "int8", "int4"]
    prec_lbls  = {"fp16": "FP16", "bf16": "BF16", "int8": "INT8", "int4": "INT4"}
    lv_order   = ["Level_1", "Level_5"]
    lv_lbls    = {"Level_1": "L1 (easy)", "Level_5": "L5 (hard)"}

    for ax, lv in zip(axes, lv_order):
        if lv not in p3:
            ax.set_visible(False)
            continue
        lv_data  = p3[lv]
        tstars   = lv_data.get("T_stars", {})
        precs_av = lv_data.get("precisions_available", [])
        vals = [(p, tstars[p]/1000) for p in prec_order if p in tstars]
        if not vals:
            ax.set_visible(False)
            continue
        precs_plot, ts_plot = zip(*vals)
        cols = [PREC_COLORS[p] for p in precs_plot]
        bars = ax.bar(precs_plot, ts_plot, color=cols, alpha=0.85, width=0.5)
        for bar, val in zip(bars, ts_plot):
            ax.text(bar.get_x() + bar.get_width()/2, val + 0.05,
                    f"{val:.1f}K", ha="center", fontsize=9, fontweight="bold")
        mono = lv_data.get("monotone_decreasing", False)
        ax.set_title(f"{lv_lbls.get(lv, lv)} — monotone: {mono}", fontsize=11)
        ax.set_xlabel("Precision", fontsize=10)
        ax.set_ylabel("$T^*$ (thousands of tokens)", fontsize=10)
        ax.set_ylim(0, max(ts_plot) * 1.25)

    fig.tight_layout()
    out = os.path.join(FIGS, "fig_expD_precision.png")
    fig.savefig(out, bbox_inches="tight", dpi=200)
    plt.close(fig)
    print(f"  saved → {out}")

    # Write LaTeX table
    tex = (
        "% Auto-generated by generate_tables.py from results/precision_results_expD.json\n"
        "\\begin{table}[t]\n"
        "\\centering\\small\n"
        "\\caption{Exp D (P3): Fitted $T^*$ by numerical precision and difficulty. "
        "P3 predicts $T^*$ should decrease monotonically with lower precision; "
        "neither level shows strict monotonicity at small scale.}\n"
        "\\label{tab:expD_precision}\n"
        "\\begin{tabular}{lccccl}\n"
        "\\toprule\n"
        "Level & FP16 & BF16 & INT8 & INT4 & Monotone $\\downarrow$? \\\\\n"
        "\\midrule\n"
    )
    for lv in lv_order:
        if lv not in p3:
            continue
        tstars = p3[lv].get("T_stars", {})
        mono   = p3[lv].get("monotone_decreasing", False)
        vals   = " & ".join(
            f"{tstars[p]:.0f}" if p in tstars else "--"
            for p in prec_order
        )
        mono_str = "\\checkmark" if mono else "\\texttimes"
        tex += f"{lv_lbls.get(lv, lv)} & {vals} & {mono_str} \\\\\n"
    tex += "\\bottomrule\n\\end{tabular}\n\\end{table}\n"
    with open(os.path.join(TABLES, "tab_expD_precision.tex"), "w") as f:
        f.write(tex)
    print("  saved → results/tab_expD_precision.tex")


# Updated prediction-status table from real data
def write_prediction_status():
    a = load("multimodel_results_expA.json")
    b = load("p6_results_expB.json")
    c = load("hurst_results_expC.json")
    d = load("precision_results_expD.json")
    e = load("prospective_results_expE.json")

    # P1 evidence
    p1_qwen = a.get("P1_per_model", {}).get("qwen3b", {})
    p1_mono = p1_qwen.get("monotonic", False)
    p1_ratio = p1_qwen.get("ratio", None)
    p1_status = "Mixed" if not p1_mono else "Partial support"
    p1_note = (f"Qwen3B T*(L5)/T*(L1)={p1_ratio:.2f}; "
               "Phi-3.5 L1 ceiling (100\\%)" if p1_ratio else "Ceiling at L1; Qwen3B non-monotone")

    # P3 evidence
    p3_lv5 = d.get("P3_tests", {}).get("Level_5", {})
    p3_mono = p3_lv5.get("monotone_decreasing", False)
    p3_ts   = p3_lv5.get("T_stars", {})
    p3_note = (f"FP16={p3_ts.get('fp16',0):.0f}, INT8={p3_ts.get('int8',0):.0f}; "
               "non-monotone at L5")

    # P5 evidence
    ph_l3 = c.get("phi35", {}).get("Level_3", [])
    H_est  = "\\approx 0" if ph_l3 else "N/A"
    p5_note = "V(T) flat; $\\hat{H}\\approx 0$, not 0.5"

    # P6 evidence
    p6_lv1 = b.get("P6_tests", {}).get("Level_1", {})
    p6_mono = p6_lv1.get("monotone_decreasing_with_N", False)
    tstars  = p6_lv1.get("T_stars", {})
