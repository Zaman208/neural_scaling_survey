import json
import os
import numpy as np
import matplotlib.pyplot as plt
import matplotlib.ticker as ticker

ROOT    = os.path.join(os.path.dirname(__file__), "..")
RESULTS = os.path.join(ROOT, "results")
FIGS    = os.path.join(ROOT, "figures")
TABLES  = os.path.join(ROOT, "results")  
os.makedirs(FIGS, exist_ok=True)

plt.rcParams.update({
    "font.family": "serif", "font.size": 11,
    "axes.spines.top": False, "axes.spines.right": False,
    "lines.linewidth": 2.0, "figure.dpi": 150,
})

BLUE   = "#2166AC"; RED    = "#D6604D"; GREEN  = "#4DAC26"
ORANGE = "#F4A582"; PURPLE = "#762A83"; TEAL   = "#1B9E77"

MODEL_LABELS = {"phi35": "Phi-3.5-mini (3.8B)", "qwen3b": "Qwen2.5-3B",
                "qwen_1b5": "Qwen2.5-1.5B", "qwen_3b": "Qwen2.5-3B"}
LEVEL_COLORS = {"Level_1": BLUE, "Level_3": ORANGE, "Level_5": RED}
LEVEL_LABELS = {"Level_1": "L1 (easy)", "Level_3": "L3 (med)", "Level_5": "L5 (hard)"}
PREC_COLORS  = {"fp16": BLUE, "bf16": GREEN, "int8": ORANGE, "int4": RED}


def load(name):
    path = os.path.join(RESULTS, name)
    with open(path) as f:
        return json.load(f)


# =============================================================================
# Exp A — accuracy vs. budget (P1 test)
# =============================================================================
def process_expA():
    data = load("multimodel_results_expA.json")
    models_raw = data.get("models", {})

    fig, axes = plt.subplots(1, 2, figsize=(12, 4.5))
    fig.suptitle("Exp A: Accuracy vs. Budget by Difficulty\n"
                 "(Phi-3.5-mini & Qwen2.5-3B, MATH-500)", fontsize=12)

    model_order = [("phi35", axes[0]), ("qwen3b", axes[1])]
    table_rows  = []

    for (mkey, ax) in model_order:
        mdata = models_raw.get(mkey, {})
        if not mdata:
            ax.set_visible(False)
            continue
        label = MODEL_LABELS.get(mkey, mkey)
        ax.set_title(label, fontsize=11, fontweight="bold")

        for lv, col in LEVEL_COLORS.items():
            series = mdata.get(lv, [])
            if not series:
                continue
            budgets = [pt["budget"] for pt in series]
            accs    = [pt["accuracy"] * 100 for pt in series]
            ax.plot(budgets, accs, color=col, lw=2,
                    marker="o", ms=5, label=LEVEL_LABELS[lv])
            # collect for table
            for b, a in zip(budgets, accs):
                table_rows.append((mkey, lv, b, a))

        ax.set_xlabel("Budget T (tokens)", fontsize=10)
        ax.set_ylabel("Accuracy (%)", fontsize=10)
        ax.set_xscale("log")
        ax.legend(fontsize=9)
        ax.set_ylim(0, 105)

    fig.tight_layout()
    out = os.path.join(FIGS, "fig_expA_accuracy.png")
    fig.savefig(out, bbox_inches="tight", dpi=200)
    plt.close(fig)
    print(f"  saved → {out}")

    # P1 summary from stored fits
    p1 = data.get("P1_per_model", {})
    print("\n  Exp A — P1 summary (stored fits):")
    for m, lv_dict in p1.items():
        print(f"    {m}: {lv_dict}")

    # Write LaTeX table snippet
    tex = (
        "% Auto-generated by generate_tables.py from results/multimodel_results_expA.json\n"
        "\\begin{table}[t]\n"
        "\\centering\\small\n"
        "\\caption{Exp A: Accuracy (\\%) vs.\\ budget by model and difficulty level "
        "(MATH-500, 8 problems per level).}\n"
        "\\label{tab:expA_accuracy}\n"
        "\\begin{tabular}{llrrrrrrr}\n"
        "\\toprule\n"
        "Model & Level & \\multicolumn{7}{c}{Budget T} \\\\\n"
        "\\cmidrule{3-9}\n"
        " & & 150 & 300 & 600 & 1200 & 2400 & 4800 & 9600 \\\\\n"
        "\\midrule\n"
    )
    for mkey, ax in model_order:
        mdata = models_raw.get(mkey, {})
        for lv in ["Level_1", "Level_3", "Level_5"]:
            series = mdata.get(lv, [])
            acc_by_budget = {pt["budget"]: pt["accuracy"] * 100 for pt in series}
            cells = " & ".join(
                f"{acc_by_budget.get(b, float('nan')):.0f}"
                if not np.isnan(acc_by_budget.get(b, float("nan"))) else "--"
                for b in [150, 300, 600, 1200, 2400, 4800, 9600]
            )
            mlabel = MODEL_LABELS.get(mkey, mkey).split(" ")[0]
            tex += f"{mlabel} & {LEVEL_LABELS[lv]} & {cells} \\\\\n"
        tex += "\\midrule\n"
    tex += "\\bottomrule\n\\end{tabular}\n\\end{table}\n"

    with open(os.path.join(TABLES, "tab_expA_accuracy.tex"), "w") as f:
        f.write(tex)
    print("  saved → results/tab_expA_accuracy.tex")
    return table_rows


